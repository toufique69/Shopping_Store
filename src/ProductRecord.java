    private void product_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_product_tableMouseClicked
        // TODO add your handling code here:
        try{
            connect = SQLServerConnect.ConnectDB("sa", "123456789", "MOBILE_SHOP");
            
            int row= product_table.getSelectedRow();
            String table_click= product_table.getModel().getValueAt(row, 0).toString();
            String sql= "select * from PRODUCTS where ProductId = '" + table_click + "'";
            
            rs = SQLServerConnect.SearchDB(connect, sql);
            if(rs.next()){
                this.hide();
                Products ce = new Products();
                ce.setVisible(true);
                ce.PIUpdate.setEnabled(true);
                ce.PIDelete.setEnabled(true);
                ce.PISave.setEnabled(false);
                ce.txtProductId.setEditable(false);
                ce.flag = 1;
                
                String add1=rs.getString("ProductId");
                ce.txtProductId.setText(add1);
                
                String add2=rs.getString("Brand");
                ce.txtBrandName.setText(add2);
                
                String add3=rs.getString("Model");
                ce.txtModelName.setText(add3);
                
                String add4=rs.getString("ProductDescription");
                ce.txtProductDescription.setText(add4);
                
                String add5=rs.getString("Price");
                ce.txtUnitPrice.setText(add5);
                
                String add6=rs.getString("Available");
                ce.cmbAvailability.setSelectedItem(add6);
                
                
                String add7=rs.getString("ProductCode");
                ce.txtProductCode.setText(add7);
                
                String add8=rs.getString("CategoryId");
                ce.txtCategoryId.setText(add8);
                
               
                
                              
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,e);
        }
    }//GEN-LAST:event_product_tableMouseClicked

