    private void GetTableData() {
        String sql="select ProductId as [Product Id], Brand as [Product Name], Model as [Category], ProductDescription as [Description], Price, Available, ProductCode as [Product Code], CategoryId as [Brand ID] from PRODUCTS";
        try{
            rs = SQLServerConnect.SearchDB(connect, sql);
            product_table.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);        
        }
    }
